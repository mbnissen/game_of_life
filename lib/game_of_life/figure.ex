    text
    |> String.split("\n", trim: true)
    |> Enum.with_index(1)
    |> Enum.map(fn {line, y} ->
      String.graphemes(line)
      |> Enum.with_index(1)
      |> Enum.map(fn
        {"O", x} -> {x, y}
        {".", _} -> nil
      end)
      |> Enum.filter(&(!is_nil(&1)))
    end)
    |> List.flatten()
  end

